      SUBROUTINE CELSS(LGIT, TARGT, LOSS)
      REAL LGIT(8,10), LOSS
      INTEGER TARGT(8)
      INTEGER I, J, TK
      REAL VMAX, ESUM, P
      LOSS = 0.0
      DO 10 I = 1, 8
          VMAX = LGIT(I, 1)
          DO 11 J = 2, 10
              IF (LGIT(I,J) - VMAX) 11, 11, 12
 12           VMAX = LGIT(I, J)
 11       CONTINUE
          ESUM = 0.0
          DO 13 J = 1, 10
              ESUM = ESUM + EXP(LGIT(I,J) - VMAX)
 13       CONTINUE
          TK = TARGT(I) + 1
          P = EXP(LGIT(I,TK) - VMAX) / ESUM
          IF (P - 0.0001) 14, 15, 15
 14       P = 0.0001
 15       LOSS = LOSS - ALOG(P)
 10   CONTINUE
      LOSS = LOSS / 8.0
      RETURN
      END
