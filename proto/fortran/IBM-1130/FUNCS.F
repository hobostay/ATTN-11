      SUBROUTINE SFTMX(VEC, N, OUT)
      REAL VEC(10), OUT(10)
      INTEGER N, I
      REAL VMAX, ESUM
C FIND MAX
      VMAX = VEC(1)
      DO 10 I = 2, N
          IF (VEC(I) - VMAX) 10, 10, 11
 11       VMAX = VEC(I)
 10   CONTINUE
C EXP AND SUM
      ESUM = 0.0
      DO 20 I = 1, N
          OUT(I) = EXP(VEC(I) - VMAX)
          ESUM = ESUM + OUT(I)
 20   CONTINUE
C NORMALIZE
      DO 30 I = 1, N
          OUT(I) = OUT(I) / ESUM
 30   CONTINUE
      RETURN
      END
